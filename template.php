					<p>Our club was founded in the Spring of 2011 to compete in the 2012 NASA University Student Launch Initiative Competition.  The challenge for this event was to develop and build a high powered rocket that would reach as close to one mile as possible and would carry a scientific payload.  Our 2012 Rocket was over 8 feet tall and could carry a payload that included a GPS, an accelerometer, a camera and a set of dynamically adjusted drag flaps to accurately reach the required 5280 ft.  This year the Rensselaer Rocket Society is competing in the  Intercollegiate Rocket Engineering Competition (IREC).  This Competition involves launching a 10 lb  scientific payload to 10,000 ft.</p>

